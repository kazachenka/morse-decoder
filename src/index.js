const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
    ' ':      ' ',
};

function decode(expr) {
        let arr = [];
    let t = [];
    let res = '';
    for (let i = 0; i < expr.length/10; i++){
        arr[i] = expr.substr(i*10,10);
        if( arr[i] === '**********' ) {
            t[i] = ' ';
        }
        else {
            t[i] = '';
            for (let j = 0 ; j < 5; j++) {
                arr[i].substr(j*2,2) === '11' ? t[i] += '-' : t[i];
                arr[i].substr(j*2,2) === '10' ? t[i] += '.' : t[i];
            }
        }
    }
    for (let i = 0; i < t.length; i++){
        res += MORSE_TABLE[`${t[i]}`];
    }
    return res;
}

module.exports = {
    decode
}
